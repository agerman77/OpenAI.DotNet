using OpenAI.DotNet.Enums;

namespace OpenAI.DotNet.Models.Requests
{
    public class ChatCompletionRequest
    {

        public ChatCompletionRequest(List<ChatCompletionMessage> messages, string model)
        {
            Messages = messages;
            Model = model;
        }

        /// <summary>
        /// A list of messages comprising the conversation so far
        /// </summary>
        public List<ChatCompletionMessage> Messages { get; private set; }

        /// <summary>
        /// ID of the model to use. See the model endpoint compatibility table for details on which models work with the Chat API.
        /// <see href="https://platform.openai.com/docs/models/model-endpoint-compatibility"/>
        /// </summary>
        public string Model { get; private set; }

        /// <summary>
        /// number or null | Optional Defaults to 0
        /// Number between -2.0 and 2.0. Positive values penalize new tokens based on their existing frequency in the text so far, decreasing the model's likelihood to repeat the same line verbatim.
        /// <see href="https://platform.openai.com/docs/guides/text-generation/parameter-details"/>
        /// </summary>
        public int? FrequencyPenalty { get; set; }

        /// <summary>
        /// map | Optional Defaults to null
        /// Modify the likelihood of specified tokens appearing in the completion. 
        /// Accepts a JSON object that maps tokens (specified by their token ID in the tokenizer) to an associated bias value from -100 to 100. Mathematically, the bias is added to the logits generated by the model prior to sampling. The exact effect will vary per model, but values between -1 and 1 should decrease or increase likelihood of selection; values like -100 or 100 should result in a ban or exclusive selection of the relevant token.
        /// </summary>
        public object? LogitBias { get; set; }

        /// <summary>
        /// integer or null | Optional | Defaults to inf
        /// The maximum number of tokens to generate in the chat completion.
        /// The total length of input tokens and generated tokens is limited by the model's context length.
        /// </summary>
        public int? MaxTokens { get; set; }

        /// <summary>
        /// How many chat completion choices to generate for each input message.
        /// </summary>
        public int N { get; set; } = 1;

        /// <summary>
        /// Number between -2.0 and 2.0. Positive values penalize new tokens based on whether they appear in the text so far, increasing the model's likelihood to talk about new topics.
        ///<see href="https://platform.openai.com/docs/guides/text-generation/parameter-details"/>
        /// </summary>
        public int? PresencePenalty { get; set; }

        /// <summary>
        /// An object specifying the format that the model must output.
        /// Setting to { "type": "json_object" } enables JSON mode, which guarantees the message the model generates is valid JSON.
        /// Important: when using JSON mode, you must also instruct the model to produce JSON yourself via a system or user message. Without this, the model may generate an unending stream of whitespace until the generation reaches the token limit, resulting in a long-running and seemingly "stuck" request. Also note that the message content may be partially cut off if finish_reason="length", which indicates the generation exceeded max_tokens or the conversation exceeded the max context length.
        /// </summary>
        public ResponseFormat? ResponseFormat { get; set; } //= ResponseFormat.Text;

        /// <summary>
        /// integer or null | Optional
        /// This feature is in Beta. If specified, our system will make a best effort to sample deterministically, such that repeated requests with the same seed and parameters should return the same result. Determinism is not guaranteed, and you should refer to the system_fingerprint response parameter to monitor changes in the backend.
        /// </summary>
        public int? Seed { get; set; }

        /// <summary>
        /// string | array | null Optional Defaults to null
        /// Up to 4 sequences where the API will stop generating further tokens.
        /// </summary>
        public object? Stop { get; set; }

        /// <summary>
        /// boolean or null | Optional | defaults to false
        /// If set, partial message deltas will be sent, like in ChatGPT. Tokens will be sent as data-only <see langword="server-sent events" href="https://developer.mozilla.org/en-US/docs/Web/API/Server-sent_events/Using_server-sent_events#Event_stream_format"/> as they become available, with the stream terminated by a <code>data: [DONE]</code> message. <see langword="Example Python code" href="https://cookbook.openai.com/examples/how_to_stream_completions"/>.
        /// 
        /// </summary>
        public bool? Stream { get; set; }

        /// <summary>
        /// number or null | Optional | Defaults to 1
        /// What sampling temperature to use, between 0 and 2. Higher values like 0.8 will make the output more random, while lower values like 0.2 will make it more focused and deterministic.
        /// We generally recommend altering this or top_p but not both.
        /// </summary>
        public double? Temperature { get; set; }// = 1;

        /// <summary>
        /// number or null | Optional | Defaults to 1
        /// An alternative to sampling with temperature, called nucleus sampling, where the model considers the results of the tokens with top_p probability mass. So 0.1 means only the tokens comprising the top 10% probability mass are considered.
        /// We generally recommend altering this or temperature but not both.
        /// </summary>
        public int? TopP { get; set; }// = 1;

        /// <summary>
        /// array | Optional
        /// A list of tools the model may call. Currently, only functions are supported as a tool. Use this to provide a list of functions the model may generate JSON inputs for.
        /// </summary>
        public List<Tool>? Tools { get; set; }

        /// <summary>
        /// string or object | Optional
        /// Controls which (if any) function is called by the model. 
        /// none means the model will not call a function and instead generates a message. auto means the model can pick between generating a message or calling a function. Specifying a particular function via <code>{"type: "function", "function": {"name": "my_function"}}</code>  forces the model to call that function.
        ///none is the default when no functions are present.auto is the default if functions are present.
        /// </summary>
        public string? ToolChoice { get; set; }

        /// <summary>
        /// A unique identifier representing your end-user, which can help OpenAI to monitor and detect abuse.
        /// <see langword="Learn more: " href="https://platform.openai.com/docs/guides/safety-best-practices/end-user-ids"/>
        /// </summary>
        public string User { get; set; }
    }
}